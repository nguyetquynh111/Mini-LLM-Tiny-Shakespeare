"""Generate prompt completions for both local Transformer checkpoints."""

from __future__ import annotations

import argparse
import sys
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

import torch

from configs import config_from_dict, get_default_device
from data import decode, encode
from model import GPTLanguageModel


EVALUATION_DIR = REPO_ROOT / "evaluation"
DEFAULT_PROMPTS_PATH = EVALUATION_DIR / "prompts.txt"
DEFAULT_CHECKPOINT_DIR = REPO_ROOT / "checkpoints"
MAX_NEW_TOKENS = 150


def read_prompts(path: Path) -> list[str]:
    """Read non-empty prompts from a plain text file."""
    if not path.exists():
        raise FileNotFoundError(f"Missing prompts file: {path}")

    prompts = [line.strip() for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]
    if not prompts:
        raise ValueError(f"Prompts file is empty: {path}")
    return prompts


def load_model(checkpoint_path: Path, device: str) -> GPTLanguageModel:
    """Load one trained model from a checkpoint."""
    if not checkpoint_path.exists():
        raise FileNotFoundError(f"Missing checkpoint: {checkpoint_path}")

    checkpoint = torch.load(checkpoint_path, map_location=device)
    config = config_from_dict(checkpoint["config"])
    config.device = device

    model = GPTLanguageModel(config).to(device)
    model.load_state_dict(checkpoint["model_state_dict"])
    model.eval()
    return model


@torch.no_grad()
def generate_for_prompts(model: GPTLanguageModel, prompts: list[str], device: str) -> list[tuple[str, str]]:
    """Generate exactly 150 new tokens for each prompt."""
    outputs: list[tuple[str, str]] = []
    for prompt in prompts:
        prompt_tokens = encode(prompt)
        idx = torch.tensor([prompt_tokens], dtype=torch.long, device=device)
        generated = model.generate(idx, MAX_NEW_TOKENS)
        outputs.append((prompt, decode(generated[0].tolist())))
    return outputs


def write_generations(path: Path, model_label: str, outputs: list[tuple[str, str]]) -> None:
    """Write generated text samples to a readable text file."""
    lines = [f"{model_label} generations", f"Each sample uses exactly {MAX_NEW_TOKENS} new generated byte tokens.", ""]
    for prompt, output in outputs:
        lines.extend(
            [
                "=" * 80,
                f"Prompt: {prompt}",
                "-" * 80,
                output,
                "",
            ]
        )
    path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Saved generations to {path}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate prompt completions for local checkpoints.")
    parser.add_argument("--prompts", type=Path, default=DEFAULT_PROMPTS_PATH)
    parser.add_argument("--checkpoint_dir", type=Path, default=DEFAULT_CHECKPOINT_DIR)
    parser.add_argument("--device", type=str, default=None)
    parser.add_argument("--seed", type=int, default=1337)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    device = args.device or get_default_device()
    torch.manual_seed(args.seed)
    if device == "cuda":
        torch.cuda.manual_seed_all(args.seed)

    prompts = read_prompts(args.prompts)
    for model_name, model_label in (("model_a", "Custom Model A"), ("model_b", "Custom Model B")):
        model = load_model(args.checkpoint_dir / f"{model_name}.pt", device)
        outputs = generate_for_prompts(model, prompts, device)
        write_generations(EVALUATION_DIR / f"generations_{model_name}.txt", model_label, outputs)


if __name__ == "__main__":
    main()

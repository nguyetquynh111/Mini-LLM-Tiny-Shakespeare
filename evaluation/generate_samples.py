"""Generate prompt completions for both local Transformer checkpoints."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

import torch

from mini_llm.configs import config_from_dict, get_default_device
from mini_llm.data import decode, encode
from mini_llm.model import GPTLanguageModel
from mini_llm.utils import (
    CHECKPOINT_DIR,
    EVALUATION_DIR,
    GENERATION_DIR,
    ensure_output_dirs,
    missing_checkpoint_message,
    seed_everything,
)


DEFAULT_PROMPTS_PATH = EVALUATION_DIR / "prompts.txt"
DEFAULT_CHECKPOINT_DIR = CHECKPOINT_DIR
MAX_NEW_TOKENS = 150


def read_prompts(path: Path) -> list[str]:
    """Read non-empty prompts from a plain text file."""
    if not path.exists():
        raise FileNotFoundError(f"Missing prompts file: {path}")

    prompts = [line.strip() for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]
    if not prompts:
        raise ValueError(f"Prompts file is empty: {path}")
    return prompts


def load_model(checkpoint_path: Path, device: str) -> GPTLanguageModel:
    """Load one trained model from a checkpoint."""
    if not checkpoint_path.exists():
        raise FileNotFoundError(missing_checkpoint_message(checkpoint_path.stem, checkpoint_path))

    checkpoint = torch.load(checkpoint_path, map_location=device)
    config = config_from_dict(checkpoint["config"])
    config.device = device

    model = GPTLanguageModel(config).to(device)
    model.load_state_dict(checkpoint["model_state_dict"])
    model.eval()
    return model


def generated_new_token_count(prompt: str, generated_text: str) -> int:
    """Return generated byte-token count after the prompt."""
    return len(encode(generated_text)) - len(encode(prompt))


def verify_generated_length(prompt: str, generated_text: str, expected_new_tokens: int) -> None:
    """Raise if a local generation does not contain the expected new byte tokens."""
    actual = generated_new_token_count(prompt, generated_text)
    if actual != expected_new_tokens:
        raise ValueError(f"Expected {expected_new_tokens} new byte tokens, found {actual}")


def verify_generation_record_length(record: dict[str, object], expected_new_tokens: int = MAX_NEW_TOKENS) -> None:
    """Raise if a structured local generation record has the wrong byte-token count."""
    actual = int(record.get("actual_generated_new_token_count", -1))
    if actual != expected_new_tokens:
        raise ValueError(f"Expected {expected_new_tokens} new byte tokens, found {actual}")


@torch.no_grad()
def generate_for_prompts(
    model: GPTLanguageModel,
    prompts: list[str],
    device: str,
    max_new_tokens: int = MAX_NEW_TOKENS,
    temperature: float = 1.0,
    top_k: int | None = None,
) -> list[dict[str, object]]:
    """Generate exactly 150 new tokens for each prompt."""
    outputs: list[dict[str, object]] = []
    for prompt in prompts:
        prompt_tokens = encode(prompt)
        idx = torch.tensor([prompt_tokens], dtype=torch.long, device=device)
        generated = model.generate(idx, max_new_tokens, temperature=temperature, top_k=top_k)
        generated_tokens = generated[0].tolist()
        text = decode(generated_tokens)
        actual_new_tokens = len(generated_tokens) - len(prompt_tokens)
        if actual_new_tokens != max_new_tokens:
            raise RuntimeError(f"Generated {actual_new_tokens} new byte tokens, expected {max_new_tokens}")
        outputs.append(
            {
                "prompt": prompt,
                "generated_text": text,
                "requested_new_token_count": max_new_tokens,
                "actual_generated_new_token_count": actual_new_tokens,
                "sampling": {"temperature": temperature, "top_k": top_k},
            }
        )
    return outputs


def write_generations(path: Path, model_label: str, outputs: list[dict[str, object]]) -> None:
    """Write generated text samples to a readable text file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    token_count = outputs[0]["requested_new_token_count"] if outputs else MAX_NEW_TOKENS
    lines = [f"{model_label} generations", f"Each sample uses exactly {token_count} new generated byte tokens.", ""]
    for output in outputs:
        lines.extend(
            [
                "=" * 80,
                f"Prompt: {output['prompt']}",
                "-" * 80,
                str(output["generated_text"]),
                "",
            ]
        )
    path.write_text("\n".join(lines), encoding="utf-8")
    print(f"Saved generations to {path}")


def write_generations_jsonl(path: Path, model_name: str, checkpoint_path: Path, outputs: list[dict[str, object]]) -> None:
    """Write generated text samples as structured JSONL."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as file:
        for output in outputs:
            record = {
                **output,
                "checkpoint_path": str(checkpoint_path),
                "model_config_name": model_name,
            }
            file.write(json.dumps(record, ensure_ascii=False) + "\n")
    print(f"Saved structured generations to {path}")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate prompt completions for local checkpoints.")
    parser.add_argument("--prompts", type=Path, default=DEFAULT_PROMPTS_PATH)
    parser.add_argument("--checkpoint_dir", type=Path, default=DEFAULT_CHECKPOINT_DIR)
    parser.add_argument("--max-new-tokens", "--max_new_tokens", dest="max_new_tokens", type=int, default=MAX_NEW_TOKENS)
    parser.add_argument("--temperature", type=float, default=1.0)
    parser.add_argument("--top-k", "--top_k", dest="top_k", type=int, default=None)
    parser.add_argument("--device", type=str, default=None)
    parser.add_argument("--seed", type=int, default=1337)
    return parser.parse_args()


def validate_args(args: argparse.Namespace) -> None:
    """Reject invalid generation arguments with clear messages."""
    if args.max_new_tokens <= 0:
        raise ValueError("--max-new-tokens must be positive")
    if args.temperature <= 0:
        raise ValueError("--temperature must be positive")
    if args.top_k is not None and args.top_k <= 0:
        raise ValueError("--top-k must be positive when provided")


def main() -> None:
    args = parse_args()
    validate_args(args)
    device = args.device or get_default_device()
    seed_everything(args.seed)

    ensure_output_dirs()
    prompts = read_prompts(args.prompts)
    for model_name, model_label in (("model_a", "Custom Model A"), ("model_b", "Custom Model B")):
        checkpoint_path = args.checkpoint_dir / f"{model_name}.pt"
        try:
            model = load_model(checkpoint_path, device)
        except FileNotFoundError as exc:
            print(str(exc), file=sys.stderr)
            raise SystemExit(1) from exc
        outputs = generate_for_prompts(
            model,
            prompts,
            device,
            max_new_tokens=args.max_new_tokens,
            temperature=args.temperature,
            top_k=args.top_k,
        )
        write_generations(GENERATION_DIR / f"{model_name}.txt", model_label, outputs)
        write_generations_jsonl(GENERATION_DIR / f"{model_name}.jsonl", model_name, checkpoint_path, outputs)


if __name__ == "__main__":
    main()

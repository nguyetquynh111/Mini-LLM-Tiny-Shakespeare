"""Deterministic quality analysis for saved local and Gemini generations.

Local models generate byte tokens, while Gemini uses an unavailable subword
tokenizer. Quantitative text-shape metrics are therefore normalized to the first
150 UTF-8 bytes of every continuation. This does not relabel Gemini bytes as
tokens; it only gives repetition and structure metrics a common text-length unit.
"""

from __future__ import annotations

import argparse
import csv
import json
import re
import statistics
import sys
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parent.parent
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from mini_llm.data import load_data
from mini_llm.utils import EVALUATION_RESULTS_DIR, GENERATION_DIR


ANALYSIS_BYTES = 150
DEFAULT_METRICS_PATH = EVALUATION_RESULTS_DIR / "generation_metrics.csv"
DEFAULT_SUMMARY_PATH = EVALUATION_RESULTS_DIR / "generation_summary.csv"
SPEAKER_HEADING = re.compile(r"^[A-Z][A-Z '\-]{2,}:$")


def read_jsonl(path: Path) -> list[dict[str, object]]:
    """Read non-empty JSON objects from a JSONL artifact."""
    if not path.exists():
        raise FileNotFoundError(f"Missing generation artifact: {path}")
    records = [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]
    if not records:
        raise ValueError(f"Generation artifact is empty: {path}")
    return records


def duplicate_ngram_rate(data: bytes, n: int) -> float:
    """Return the fraction of n-gram occurrences beyond their first occurrence."""
    if n <= 0:
        raise ValueError("n must be positive")
    total = len(data) - n + 1
    if total <= 0:
        return 0.0
    grams = [data[index : index + n] for index in range(total)]
    return (total - len(set(grams))) / total


def corpus_ngram_overlap(data: bytes, corpus_ngrams: set[bytes], n: int = 4) -> float:
    """Return the fraction of generated n-grams observed in the training corpus."""
    total = len(data) - n + 1
    if total <= 0:
        return 0.0
    matches = sum(data[index : index + n] in corpus_ngrams for index in range(total))
    return matches / total


def longest_corpus_match(data: bytes, corpus: bytes, minimum: int = 4) -> int:
    """Return the longest contiguous generated byte span found in training text."""
    def has_match(length: int) -> bool:
        return any(data[index : index + length] in corpus for index in range(len(data) - length + 1))

    if len(data) < minimum or not has_match(minimum):
        return 0
    low, high = minimum, len(data)
    while low < high:
        middle = (low + high + 1) // 2
        if has_match(middle):
            low = middle
        else:
            high = middle - 1
    return low


def continuation_bytes(record: dict[str, object], system: str) -> bytes:
    """Extract the saved continuation without calling a model or external API."""
    if system in {"model_a", "model_b"}:
        actual = int(record.get("actual_generated_new_token_count", -1))
        if actual != ANALYSIS_BYTES:
            raise ValueError(f"{system} record must contain exactly {ANALYSIS_BYTES} generated byte tokens")
        token_ids = record.get("generated_token_ids")
        if isinstance(token_ids, list):
            values = [int(value) for value in token_ids]
            if len(values) != ANALYSIS_BYTES or any(value < 0 or value > 255 for value in values):
                raise ValueError(f"{system} generated_token_ids are invalid")
            return bytes(values)
        continuation = record.get("generated_continuation")
        if not isinstance(continuation, str):
            generated = str(record.get("generated_text", ""))
            prompt = str(record.get("prompt", ""))
            continuation = generated[len(prompt) :] if generated.startswith(prompt) else generated
        return continuation.encode("utf-8")
    returned = record.get("returned_text")
    if not isinstance(returned, str):
        raise ValueError("Gemini record is missing returned_text")
    return returned.encode("utf-8")


def measure_text(
    data: bytes,
    *,
    training_corpus: bytes,
    training_fourgrams: set[bytes],
    include_corpus_comparison: bool = True,
) -> dict[str, float | int | None]:
    """Measure repetition, structure, corpus likeness, and exact-match risk."""
    normalized = data[:ANALYSIS_BYTES]
    text = normalized.decode("utf-8", errors="replace")
    lines = [line for line in text.splitlines() if line.strip()]
    line_lengths = [len(line) for line in lines]
    return {
        "source_output_bytes": len(data),
        "analysis_bytes": len(normalized),
        "fourgram_repetition": duplicate_ngram_rate(normalized, 4),
        "eightgram_repetition": duplicate_ngram_rate(normalized, 8),
        "line_count": len(lines),
        "mean_line_length": statistics.fmean(line_lengths) if line_lengths else 0.0,
        "speaker_heading_count": sum(bool(SPEAKER_HEADING.fullmatch(line.strip())) for line in lines),
        "terminal_punctuation_count": sum(text.count(mark) for mark in ".?!"),
        "training_fourgram_overlap": (
            corpus_ngram_overlap(normalized, training_fourgrams) if include_corpus_comparison else None
        ),
        "longest_training_match_bytes": (
            longest_corpus_match(normalized, training_corpus) if include_corpus_comparison else None
        ),
        "replacement_character_count": text.count("\ufffd"),
    }


def analyze_records(
    records_by_system: dict[str, list[dict[str, object]]],
    training_corpus: bytes,
) -> list[dict[str, object]]:
    """Measure every saved output in a deterministic order."""
    training_fourgrams = {
        training_corpus[index : index + 4] for index in range(max(0, len(training_corpus) - 3))
    }
    rows: list[dict[str, object]] = []
    for system in ("model_a", "model_b", "gemini_flash"):
        for record in records_by_system[system]:
            raw = continuation_bytes(record, system)
            rows.append(
                {
                    "system": system,
                    "prompt": str(record.get("prompt", "")),
                    "normalization": "first_150_utf8_bytes",
                    **measure_text(
                        raw,
                        training_corpus=training_corpus,
                        training_fourgrams=training_fourgrams,
                    ),
                }
            )
    return rows


def corpus_control_rows(val_corpus: bytes, training_corpus: bytes) -> list[dict[str, object]]:
    """Measure deterministic non-overlapping real-corpus slices as a reference."""
    training_fourgrams = {
        training_corpus[index : index + 4] for index in range(max(0, len(training_corpus) - 3))
    }
    rows = []
    for offset in range(0, len(val_corpus) - ANALYSIS_BYTES + 1, ANALYSIS_BYTES):
        sample = val_corpus[offset : offset + ANALYSIS_BYTES]
        rows.append(
            {
                "system": "corpus_control",
                "prompt": f"validation_byte_{offset}",
                "normalization": "nonoverlapping_150_byte_slice",
                **measure_text(
                    sample,
                    training_corpus=training_corpus,
                    training_fourgrams=training_fourgrams,
                    include_corpus_comparison=False,
                ),
            }
        )
    return rows


def summarize(rows: list[dict[str, object]]) -> list[dict[str, object]]:
    """Aggregate deterministic numeric means and population standard deviations."""
    metrics = [
        "fourgram_repetition",
        "eightgram_repetition",
        "line_count",
        "mean_line_length",
        "speaker_heading_count",
        "terminal_punctuation_count",
        "training_fourgram_overlap",
        "longest_training_match_bytes",
        "replacement_character_count",
    ]
    output: list[dict[str, object]] = []
    systems = ["model_a", "model_b", "gemini_flash", "corpus_control"]
    for system in systems:
        system_rows = [row for row in rows if row["system"] == system]
        if not system_rows:
            continue
        summary: dict[str, object] = {"system": system, "sample_count": len(system_rows)}
        for metric in metrics:
            values = [float(row[metric]) for row in system_rows if row[metric] is not None]
            summary[f"mean_{metric}"] = "" if not values else f"{statistics.fmean(values):.6f}"
            summary[f"std_{metric}"] = "" if not values else f"{statistics.pstdev(values):.6f}"
        output.append(summary)
    return output


def write_csv(path: Path, rows: list[dict[str, object]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=list(rows[0]), lineterminator="\n")
        writer.writeheader()
        writer.writerows(rows)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Analyze saved generation artifacts without external API calls.")
    parser.add_argument("--model-a", type=Path, default=GENERATION_DIR / "model_a.jsonl")
    parser.add_argument("--model-b", type=Path, default=GENERATION_DIR / "model_b.jsonl")
    parser.add_argument("--gemini", type=Path, default=GENERATION_DIR / "gemini_flash.jsonl")
    parser.add_argument("--metrics-output", type=Path, default=DEFAULT_METRICS_PATH)
    parser.add_argument("--summary-output", type=Path, default=DEFAULT_SUMMARY_PATH)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    splits = load_data()
    training_corpus = bytes(splits["train"].tolist())
    validation_corpus = bytes(splits["val"].tolist())
    records = {
        "model_a": read_jsonl(args.model_a),
        "model_b": read_jsonl(args.model_b),
        "gemini_flash": read_jsonl(args.gemini),
    }
    rows = analyze_records(records, training_corpus)
    controls = corpus_control_rows(validation_corpus, training_corpus)
    summaries = summarize(rows + controls)
    write_csv(args.metrics_output, rows)
    write_csv(args.summary_output, summaries)
    print(f"Saved {len(rows)} per-sample measurements to {args.metrics_output}")
    print(f"Saved aggregate metrics and corpus controls to {args.summary_output}")


if __name__ == "__main__":
    main()
